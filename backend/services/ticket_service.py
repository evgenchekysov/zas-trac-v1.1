"""
ZAS-TRAC — Ticket Service (Business Rules)
=========================================

Данный файл является НАМЕРЕННО декларативным.
На Дне 29 здесь фиксируются ТОЛЬКО бизнес-правила.
Реализация логики будет выполнена позже.

Этот документ является нормативным источником
для всей дальнейшей реализации backend-логики заявок.

----------------------------------------------------------------
ОБЩИЕ АРХИТЕКТУРНЫЕ ПРИНЦИПЫ
----------------------------------------------------------------

1. Идентификация пользователей
- Единственный источник user_id — Supabase Auth (auth.uid).
- user_id НИКОГДА не принимается из frontend, Telegram,
  body или query запроса.
- Все действия в системе выполняются от имени
  аутентифицированного пользователя Supabase.

Примечание:
Возможные сценарии гостей, сервисных пользователей
или Telegram-связки рассматриваются как отдельный слой
и НЕ входят в core-логику заявок на данном этапе.


2. Роли и права
- Backend не реализует RBAC.
- Backend не хранит роли пользователей.
- Контроль доступа (admin / worker) осуществляется
  через Supabase RLS.
- Backend отвечает только за бизнес-правила действий.


3. Ответственность слоя service
- Принимает решения: разрешено действие или нет.
- Проверяет допустимость переходов статусов.
- Не выполняет SQL-запросы напрямую.
- Не знает о FastAPI, HTTP и формате API.


----------------------------------------------------------------
СТАТУСНАЯ МОДЕЛЬ ЗАЯВКИ
----------------------------------------------------------------

Допустимые статусы:

NEW         — новая заявка
ASSIGNED    — назначена исполнителю
IN_PROGRESS — в работе
PAUSED      — на паузе
DONE        — работы выполнены
CLOSED      — заявка закрыта (финальный статус)


----------------------------------------------------------------
СМЫСЛ СТАТУСОВ
----------------------------------------------------------------

NEW
- Заявка создана.
- Ответственный исполнитель отсутствует.
- Может быть:
  - назначена администратором,
  - взята исполнителем самостоятельно.

ASSIGNED
- Заявка назначена конкретному исполнителю
  (или исполнителю №1) администратором.
- Работа ещё не началась.
- Статус организационный, не рабочий.
- Может отсутствовать в сценариях self-service.

IN_PROGRESS
- Фактические работы начались.
- Исполнители ведут работу и учёт времени.

PAUSED
- Работы временно приостановлены.
- Возможен возврат к IN_PROGRESS без потери контекста.

DONE
- Фактические работы выполнены.
- Техническая часть задания завершена.
- Статус НЕ является финальным.
- Возможны проверка, подтверждение,
  оформление результатов.

CLOSED
- Заявка полностью завершена.
- Финальный статус.
- Любые дальнейшие действия запрещены.


----------------------------------------------------------------
ДОПУСТИМЫЕ ПЕРЕХОДЫ СТАТУСОВ
----------------------------------------------------------------

NEW → ASSIGNED
- Администратор назначает основного исполнителя,
  но работа ещё не началась.

NEW → IN_PROGRESS
- Исполнитель самостоятельно берёт заявку в работу.

ASSIGNED → IN_PROGRESS
- Назначенный исполнитель начинает работу.

IN_PROGRESS → PAUSED
- Работы временно приостановлены.

PAUSED → IN_PROGRESS
- Работы возобновлены.

IN_PROGRESS → DONE
- Фактические работы завершены.
- Переход может выполнить ЛЮБОЙ participant заявки
  (любой ремонтник, участвующий в выполнении работ).

DONE → CLOSED
- Подтверждение результата и завершение
  жизненного цикла заявки.
- Переход выполняется owner или admin.


----------------------------------------------------------------
НЕДОПУСТИМЫЕ ДЕЙСТВИЯ
----------------------------------------------------------------

- Пропуск статусов
  (например: NEW → DONE, ASSIGNED → PAUSED).

- Возврат из DONE или CLOSED.
  DONE и CLOSED являются невозвратными статусами.

- Прямое закрытие из IN_PROGRESS
  (IN_PROGRESS → CLOSED запрещено).

Если после DONE требуются доработки,
создаётся новая заявка или отдельный процесс
(change request).


----------------------------------------------------------------
РОЛЕВАЯ ЛОГИКА (ЛОГИЧЕСКАЯ, НЕ RBAC)
----------------------------------------------------------------

ticket.created_by
- Пользователь, создавший заявку.
- Может не участвовать в выполнении работ.
- Используется для истории и аудита.

ticket.owner_id
- Основной, ответственный исполнитель.
- Главный ремонтник / старший по заявке.
- Либо самостоятельно взял заявку,
  либо назначен администратором.

ticket_participant
- Пользователь, участвующий в выполнении работ.

Логические роли:
- owner       — основной (ответственный) исполнитель.
- participant — исполнитель / участник работ.
- admin       — определяется исключительно через RLS.


----------------------------------------------------------------
ПРАВИЛА ДЕЙСТВИЙ
----------------------------------------------------------------

Создание заявки
- Разрешено любому аутентифицированному пользователю.
- Создатель не становится owner автоматически.
- Owner определяется:
  - при самостоятельном взятии заявки,
  - либо при назначении администратором.

Присоединение к заявке (join)
- Пользователь может присоединиться,
  если ещё не является участником.
- Повторное присоединение запрещено.
- Owner считается participant по умолчанию.
- Admin может изменить owner при необходимости.

Выход из заявки (leave)
- Participant может выйти из заявки.
- Owner не может покинуть заявку самостоятельно.
- Admin может снять owner и назначить другого.

Завершение работ
- Любой participant может перевести заявку в DONE.

Закрытие заявки
- Только owner или admin.
- Только из статуса DONE.


----------------------------------------------------------------
НЕФУНКЦИОНАЛЬНЫЕ ТРЕБОВАНИЯ
----------------------------------------------------------------

- Все проверки выполняются до DB-операций.
- Все нарушения правил приводят к явной ошибке.
- Backend не должен "догадываться" о корректности действий.

----------------------------------------------------------------
КОНЕЦ ДОКУМЕНТА
----------------------------------------------------------------
"""
