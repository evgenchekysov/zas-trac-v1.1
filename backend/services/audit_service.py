"""
ZAS-TRAC — Audit / Event Service
===============================

ДАННЫЙ ФАЙЛ ЯВЛЯЕТСЯ АРХИТЕКТУРНЫМ КОНТРАКТОМ
ДЛЯ ФИКСАЦИИ СОБЫТИЙ СИСТЕМЫ (AUDIT / EVENTS).

Назначение сервиса:
- фиксация ФАКТОВ происходящих событий
- аудит действий пользователей
- отладка и разбор инцидентов
- источник данных для уведомлений (Telegram и др.)
- основа для отчётов и аналитики

Audit Service НЕ участвует в бизнес-логике
и НЕ влияет на выполнение операций.

Сбой логирования НЕ должен ломать
основную функциональность системы.
"""


# -------------------------------------------------
# ОБЩИЕ ПРИНЦИПЫ
# -------------------------------------------------

"""
1. Append-only
- События только добавляются.
- Обновление или удаление событий запрещено.

2. Отсутствие интерпретаций
- Audit фиксирует только ФАКТ.
- Никаких выводов, итогов, причинно-следственных оценок.
- Интерпретации выполняются во внешних сервисах.

3. Независимость
- Ticket / Session services работают независимо от Audit.
- Ошибка аудита не должна откатывать бизнес-операцию.

4. Единый источник истины о фактах
- Audit — хронологический журнал событий системы.
- Не заменяет состояние (tickets, sessions), а дополняет его.
"""


# -------------------------------------------------
# ТИПЫ СОБЫТИЙ
# -------------------------------------------------

"""
События описывают ЧТО произошло,
а не ЗАЧЕМ и не ЧТО ЭТО ЗНАЧИТ.
"""

EVENTS = [
    # ticket lifecycle
    "ticket_created",
    "ticket_status_changed",
    "ticket_joined",
    "ticket_left",

    # work sessions
    "session_started",
    "session_stopped",

    # system actions
    "session_auto_paused",
]


# -------------------------------------------------
# ОБЩИЙ ФОРМАТ СОБЫТИЯ
# -------------------------------------------------

"""
Каждое событие должно содержать:

- event_type   : тип события (см. EVENTS)
- user_id      : инициатор действия (auth.uid)
- ticket_id    : связанная заявка, если применимо
- payload      : дополнительные ФАКТИЧЕСКИЕ данные
- created_at   : время фиксации события (серверное)

Audit Service не валидирует бизнес-смысл payload.
"""


# -------------------------------------------------
# PAYLOAD — ДОПУСТИМЫЕ ПРИМЕРЫ
# -------------------------------------------------

"""
Примеры корректного payload:

ticket_status_changed:
{
    "old_status": "IN_PROGRESS",
    "new_status": "DONE"
}

session_stopped:
{
    "reason": "manual" | "auto_paused" | "status_done",
    "duration_seconds": 3600
}

Некорректно:
- "reason": "all work completed successfully"
- любые текстовые интерпретации результата
"""


# -------------------------------------------------
